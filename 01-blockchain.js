/*
 !Blockchain, nada mais eh que um banco de dados...

    Blockchain é uma tecnologia de registro distribuído que armazena 
    informações de maneira segura, transparente e imutável. 
    Aqui está um resumo dos principais componentes e conceitos:

    Blocos: Um blockchain é composto de uma sequência de blocos, 
    onde cada bloco contém um conjunto de transações. Cada bloco 
    também inclui um timestamp e um hash do bloco anterior, criando 
    uma cadeia de blocos interligados.

    Descentralização: Diferente de sistemas centralizados, onde um único 
    ente controla o banco de dados, um blockchain é mantido por uma rede 
    de computadores (nós) que verificam e validam as transações de forma 
    colaborativa.

    Criptografia: A segurança do blockchain é garantida por meio de técnicas 
    criptográficas. As transações são assinadas digitalmente e os dados são 
    protegidos contra alteração por meio de funções de hash.

    Consenso: Para que uma nova transação ou bloco seja adicionado ao blockchain, 
    os nós da rede devem chegar a um consenso. Existem vários algoritmos de consenso, 
    como Proof of Work (PoW) e Proof of Stake (PoS), que determinam como esse 
    consenso é alcançado.

    Imutabilidade: Uma vez que um bloco é adicionado ao blockchain, ele 
    não pode ser alterado sem modificar todos os blocos subsequentes, 
    o que requereria o consenso da maioria da rede. Isso torna o blockchain 
    extremamente resistente a fraudes e manipulações.

    Transparência: Todas as transações em um blockchain público são 
    visíveis para qualquer pessoa que tenha acesso à rede, aumentando a 
    transparência. Em blockchains privados ou permissionados, o acesso 
    pode ser restrito a participantes autorizados.

*/